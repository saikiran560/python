Dict = {1: 'Geeks', 2: 'For', 3: 'Geeks'}
print(Dict)



Dict = {1: 'Geeks', 2: 'For', 3: 'Geeks'}
print("Dictionary with the use of Integer Keys: ",Dict)



Dict = {'Name': 'Geeks', 1: [1, 2, 3, 4]}
print("Dictionary with the use of Mixed Keys: ",Dict)



Dict = {}
print("Empty Dictionary: ",Dict)


Dict = dict({1: 'Geeks', 2: 'For', 3: 'Geeks'})
print("Dictionary with the use of dict(): ",Dict)


Dict = dict([(1, 'Geeks'), (2, 'For')])
print("Dictionary with each item as a pair: ",Dict)


# Creating a Nested Dictionary
Dict = {1: 'Geeks', 2: 'For',3: {'A': 'Welcome', 'B': 'To', 'C': 'Geeks'}}

print(Dict)


